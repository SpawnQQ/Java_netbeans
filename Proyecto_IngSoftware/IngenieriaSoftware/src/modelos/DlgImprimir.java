    public DlgImprimir(javax.swing.JDialog parent, boolean modal) {
        super(parent, modal);
        initComponents();
        this.setLocationRelativeTo(null);
        int nroFilas = 0;
        Registro_servicio rs = new Registro_servicio();
        Conexion con = new Conexion();
        Connection reg = con.obtenerConexion();
        try {
            PreparedStatement consulta = reg.prepareStatement("SELECT * FROM registro");
            ResultSet resultado = consulta.executeQuery();
            while (resultado.next()) {
                DefaultTableModel modeloDeMiTabla = (DefaultTableModel) tbRegistro.getModel();
                modeloDeMiTabla.addRow(new Object[nroFilas]);
                tbRegistro.setValueAt(resultado.getString("id_registro"), nroFilas, 0);
                tbRegistro.setValueAt(resultado.getString("nombre"), nroFilas, 1);
                tbRegistro.setValueAt(resultado.getString("fecha"), nroFilas, 2);
                tbRegistro.setValueAt(resultado.getString("medida"), nroFilas, 3);
                tbRegistro.setValueAt(resultado.getInt("stock"), nroFilas, 4);
                contador++;
                nroFilas++;
            }
        } catch (SQLException ex) {
        }

    }

