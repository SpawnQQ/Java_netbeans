    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int nroFilas = 0;
        DefaultTableModel modeloDeMiTabla = (DefaultTableModel) tbRegistro.getModel();
        modeloDeMiTabla.addRow(new Object[nroFilas]);
        String a0, a1, a2, a3, a4;
        HSSFCell celda;
        HSSFRow fila;
        HSSFRichTextString texto;

        // Se crea el libro
        HSSFWorkbook libro = new HSSFWorkbook();

        // Se crea una hoja dentro del libro
        HSSFSheet hoja = libro.createSheet();

        fila = hoja.createRow(0);
        celda = fila.createCell((short) 0);
        texto = new HSSFRichTextString("Codigo");
        celda.setCellValue(texto);
        celda = fila.createCell((short) 1);
        texto = new HSSFRichTextString("Medicamento");
        celda.setCellValue(texto);
        celda = fila.createCell((short) 2);
        texto = new HSSFRichTextString("Fecha");
        celda.setCellValue(texto);
        celda = fila.createCell((short) 3);
        texto = new HSSFRichTextString("Medida");
        celda.setCellValue(texto);
        celda = fila.createCell((short) 4);
        texto = new HSSFRichTextString("Stock");
        celda.setCellValue(texto);
        for (nroFilas = 0; nroFilas < contador; nroFilas++) {
            a0 = tbRegistro.getValueAt(nroFilas, 0).toString();
            a1 = tbRegistro.getValueAt(nroFilas, 1).toString();
            a2 = tbRegistro.getValueAt(nroFilas, 2).toString();
            a3 = tbRegistro.getValueAt(nroFilas, 3).toString();
            a4 = tbRegistro.getValueAt(nroFilas, 4).toString();
            // Se crea una fila dentro de la hoja
            fila = hoja.createRow(nroFilas + 1);

            // Se crea una celda dentro de la fila
            celda = fila.createCell((short) 0);
            // Se crea el contenido de la celda y se mete en ella.
            texto = new HSSFRichTextString(a0);
            celda.setCellValue(texto);
            celda = fila.createCell((short) 1);
            texto = new HSSFRichTextString(a1);
            celda.setCellValue(texto);
            celda = fila.createCell((short) 2);
            texto = new HSSFRichTextString(a2);
            celda.setCellValue(texto);
            celda = fila.createCell((short) 3);
            texto = new HSSFRichTextString(a3);
            celda.setCellValue(texto);
            celda = fila.createCell((short) 4);
            texto = new HSSFRichTextString(a4);
            celda.setCellValue(texto);

        }

        // Se salva el libro.
        try {
            FileOutputStream elFichero = new FileOutputStream("Registro de Bodega.xls");
            libro.write(elFichero);
            elFichero.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        JOptionPane.showMessageDialog(this,"Se ha guardado su Excel de manera correcta");
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

